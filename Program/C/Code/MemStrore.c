//浮点数的存储

int main()
{
	int n = 5;
	//0 00000000 00000000000000000000101
	float* pFloat = (float*)&n;
	printf("n的值为：%d\n", n);
	//9
	printf("*pFloat的值为：%f\n",*pFloat);
	//0 00000000 00000000000000000000101
	//(-1)^0  *  1.00000000000000000000101*  2^-127
	//无限接近于0，结果为0.000000
	printf("*pFloat的值为：%6.2f\n", *pFloat);
	//000.00


	*pFloat = 5.0;
	//101.0
	//1.010*2^2
	//0 10000001 01000000000000000000000
	printf("n的值为：%d\n", n);
	//0 10000001 01000000000000000000000
	//1084227584
	printf("*pFloat的值为：%f\n", *pFloat);
	//(-1)^0  * 1.010*  2^(129-127)
	//1.010*2^2
	//101.0
	//9.000000
	return 0;
}
